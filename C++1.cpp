#include<iostream>
using namespace std;

int main() {

	//比较运算符
	// == != < > <= >=
	//返回1为真，返回0为假

	int a1 = 10;
	int b1 = 20;
	cout << (a1 == b1) << endl;

	int a2 = 10;
	int b2 = 20;
	cout << (a2 != b2) << endl;

	int a3 = 10;
	int b3 = 20;
	cout << (a3 < b3) << endl;

	int a4 = 10;
	int b4 = 20;
	cout << (a4 > b4) << endl;

	int a5 = 10;
	int b5 = 20;
	cout << (a5 <= b5) << endl;

	int a6 = 10;
	int b6 = 20;
	cout << (a6 >= b6) << endl;

	system("pause");

	return 0;

}
